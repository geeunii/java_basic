package jungol.출력;

public class 출력_연습문제6 {
    public static void main(String[] args) {

        int num1 = 123;
        float num2 = 6.5F;

        // 서식문자 %s
        System.out.printf("%s\n", "수를 출력할 때는 따옴표를 생략합니다.");
        System.out.printf("%d\n", num1);
        System.out.printf("%f", num2);
    }
}
