package jungol.출력;

public class 출력_연습문제7 {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;
        int sum = a + b;

        int year = 2005;
        int month = 6;
        int day = 17;

        System.out.println("수식을 출력하면 계산 결과가 출력됩니다.");
        System.out.printf("%d + %d = %d\n", a, b, sum);
        System.out.printf("내 생일은 %d년 %d월 %d일 입니다.\n", year, month, day);

    }
}
