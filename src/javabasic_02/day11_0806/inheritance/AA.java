package javabasic_02.day11_0806.inheritance;

public class AA {
    private int aa = 10;
    private int bb = 20;

    void printAA() {
        System.out.println(aa);
    }

    public int getAA() {
        return aa;
    }

    void printBB() {
        System.out.println(bb);
    }

    public int getBB() {
        return bb;
    }
}
